"""`metagx project` — scaffold a complete, runnable analysis folder from a config.

This is the end-user deliverable, generated BY the tool (not hand-assembled): given a validated
``config.yaml`` it writes the config, a sample-sheet template, a **collision-safe per-config conda
env**, an install/env-making script, a run script, an optional HPC profile, and a README.

The collision/dependency guarantee (see ``wiring`` invariant I) is enforced here by *splitting*
a config's tools into two buckets, never mixing colliders:

  * **tailored core env** — the tools that live in the curated, collision-free ``environment.yml``
    (``report.active_core_packages``). Written to ``env.yaml`` and put on PATH.
  * **``--use-conda`` tools** — everything the config activates that is NOT in the core env; each
    has its own isolated ``workflow/envs/*.yaml`` (that's *why* it's isolated — size or a
    dependency clash like abricate's samtools-0.1.x pin). Snakemake provisions these per-rule.

``use_conda`` is turned on **iff** the config needs any per-rule tool, so a pure-core run (e.g.
gut-profiling) creates no per-rule envs at all, and a run that needs GTDB-Tk/AMR/etc. self-provisions
exactly those — for *any* config, automatically.
"""
from __future__ import annotations

import os
from typing import Any, Dict, List, Optional

import yaml

from . import report, runner, schedulers

# The install source baked into every generated 00_setup.sh. Kept in ONE place and overridable at
# run time via the METAGX_REPO env var — point it at the canonical repo or a pinned release tag for
# a reproducible hand-off (a moving default means a folder handed out today may install a different
# metagx tomorrow).
METAGX_REPO_URL = "https://github.com/MdRasheduz-zaman/metagenomics-skill.git"

_SHORT_PLATFORMS = {"illumina", "mgi", "bgi"}


def plan_env(cfg: Dict[str, Any], env_yml_path: Optional[str]) -> Dict[str, Any]:
    """Collision-safe environment plan for a config (pure; no IO beyond reading env_yml_path).

    Returns ``{env_name, core_packages, per_rule_tools, use_conda}``. A tool is a ``per_rule``
    tool exactly when its conda package is not in the tailored core env — and invariant I
    guarantees every such tool has an isolated ``workflow/envs/*.yaml``, so ``--use-conda`` can
    provision it. ``use_conda`` is therefore the honest "does this config need per-rule envs?"."""
    active = [t for t in set(report.active_tools(cfg)) if t != "snakemake"]
    core_bases = set(report._env_yaml_packages(env_yml_path))
    per_rule = sorted(t for t in active if report.conda_package(t) not in core_bases)
    return {
        "env_name": f"metagx-{cfg.get('project', 'run')}",
        "core_packages": report.active_core_packages(cfg, env_yml_path),
        "per_rule_tools": per_rule,
        "use_conda": bool(per_rule),
    }


